    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        FindData fd = new FindData(tc.getConnection(), "select * from login where username = '" + txtUserName.getText() + "'");
        
        if(txtUserName.getText().contains(" "))
            JOptionPane.showMessageDialog(null, "Username should not contains space");
        if(txtPassword.getText().equals("") || txtPassword2.getText().equals(""))
            JOptionPane.showMessageDialog(null, "Please, type your password");
        else if(!txtPassword.getText().equals(txtPassword2.getText()))
            JOptionPane.showMessageDialog(null, "Password doesn't match");
        else
        {
            if(fd.isSuccess())
            {
                JOptionPane.showMessageDialog(null, "Username not available");
            }
            else
            {
                String table = "user";
                Object[] column = {"username", "password", "status"};
                Object[] data = {txtUserName.getText(), txtPassword.getText(), cmbStatus.getSelectedItem().toString()};
                new InsertData(tc.getConnection(), table, column, data, jTable1);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

