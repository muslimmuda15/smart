    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        if(status == 0)    // if insert
        {
            String table = "kriteria";
            Object[] column = {"id_kriteria", "kriteria", "low_bobot", "high_bobot"};
            Object[] data = {id.getText(), kriteria.getText(), lowBobot.getText(), highBobot.getText()};
            new InsertData(tc.getConnection(), table, column, data, jTable1);
        }
        else{
            String table = "kriteria";
            Object[] column = {"id_kriteria", "kriteria", "low_bobot", "high_bobot"};
            Object[] data = {id.getText(), kriteria.getText(), lowBobot.getText(), highBobot.getText()};
            new UpdateData(tc.getConnection(), table, column, data, jTable1);
        }
        design.DataBobot.lowBobot.setText(new CountData("kriteria", "low_bobot").getCount());
        design.DataBobot.highBobot.setText(new CountData("kriteria", "high_bobot").getCount());
        dispose();
    }//GEN-LAST:event_okButtonActionPerformed

